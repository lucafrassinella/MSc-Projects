function plotSettings
%-------------------------------------------------------------------------%
% Settings for figure plots
%-------------------------------------------------------------------------%

% Setting Lines:
set(0, 'defaultLineLineWidth', 1.3);

% Setting Interpreter:
set(0, 'defaultTextInterpreter', 'latex')
set(0, 'defaultLegendInterpreter', 'latex')
set(0, 'defaultAxesTickLabelInterpreter', 'latex')

% Setting Legends:
set(0, 'defaultLegendLocation','best');
set(0, 'defaultLegendFontSize', 18);

% Setting Axes:
set(0, 'defaultAxesXMinorGrid','on');
set(0, 'defaultAxesYMinorGrid','on');
set(0, 'defaultAxesFontSize', 20);  

% Title and label font size multipliers:
set(0, 'defaultAxesTitleFontWeight', 'normal');
set(0, 'defaultAxesTitleFontSizeMultiplier', 23/20);  
set(0, 'defaultAxesLabelFontSizeMultiplier', 1);     
end





